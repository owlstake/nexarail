# ---------------------------------------------------------------------------
# Safety validators
# ---------------------------------------------------------------------------
check_safety() {
    local command="$1"
    local dir="$2"

    # burn routing
    if [[ "$command" == *burn-routing* ]] && [[ "$command" == enable-* ]]; then
        load_settlement_params "$dir/safety-check"
        local live treasury
        live="$(json_get "$dir/safety-check/current-settlement-params.json" '.live_enabled // "false"')"
        treasury="$(json_get "$dir/safety-check/current-settlement-params.json" '.treasury_routing_enabled // "false"')"
        if ! is_true "$live"; then
            die "SAFETY: burn routing requires settlement live_enabled=true (currently $live). Enable settlement live first."
        fi
        if ! is_true "$treasury"; then
            die "SAFETY: burn routing requires treasury_routing_enabled=true (currently $treasury). Enable treasury routing first."
        fi
        ok "safety check passed: settlement live=$live treasury_routing=$treasury"
    fi

    # treasury routing
    if [[ "$command" == *treasury-routing* ]] && [[ "$command" == enable-* ]]; then
        load_settlement_params "$dir/safety-check"
        local live
        live="$(json_get "$dir/safety-check/current-settlement-params.json" '.live_enabled // "false"')"
        if ! is_true "$live"; then
            die "SAFETY: treasury routing requires settlement live_enabled=true (currently $live). Enable settlement live first."
        fi
        ok "safety check passed: settlement live=$live"
    fi

    rm -rf "$dir/safety-check" 2>/dev/null || true
}

# ---------------------------------------------------------------------------
# Proposal builders — produce the full MsgUpdateParams JSON
# ---------------------------------------------------------------------------
build_settlement_proposal_msg() {
    local dir="$1"
    local live="$2"
    local treasury="$3"
    local burn="$4"
    load_settlement_params "$dir"
    local params_json
    params_json="$(cat "$dir/current-settlement-params.json")"

    # Override the target flags
    params_json="$(echo "$params_json" | jq \
        --argjson live "$live" \
        --argjson treasury "$treasury" \
        --argjson burn "$burn" \
        '.live_enabled = $live | .treasury_routing_enabled = $treasury | .burn_routing_enabled = $burn')"

    cat <<EOF
    {
      "@type": "/nexarail.settlement.v1.MsgUpdateParams",
      "authority": "$GOV_ADDR",
      "params": $params_json
    }
EOF
}

