    fi
    echo ""
}

# ---------------------------------------------------------------------------
# Evidence writing
# ---------------------------------------------------------------------------
write_evidence() {
    local command="$1"
    local label="$2"
    local proposal_id="${3:-}"
    local tx_hash="${4:-}"
    local dir="$EVIDENCE_DIR"
    local evidence_file="$dir/result.json"

    cat > "$evidence_file" <<EOF
{
  "command": "$command",
  "label": "$label",
  "proposal_id": $(if [ -n "$proposal_id" ]; then echo "\"$proposal_id\""; else echo "null"; fi),
  "tx_hash": $(if [ -n "$tx_hash" ]; then echo "\"$tx_hash\""; else echo "null"; fi),
  "chain_id": "$CHAIN_ID",
  "confirm": $CONFIRM,
  "timestamp_utc": "$(date -u +%Y-%m-%dT%H:%M:%SZ)",
  "evidence_dir": "$dir",
  "network": "agent-testnet"
}
EOF

    # Also dump current flags for context
    show_live_flags "$dir/after-flags" > "$dir/after-flags-output.txt" 2>&1 || true

    ok "evidence written: $evidence_file"
}

# ---------------------------------------------------------------------------
# Main dispatcher
# ---------------------------------------------------------------------------
main() {
    local command=""
    local custom_chain=""

    # Parse arguments (positional: command first)
    while [ "$#" -gt 0 ]; do
        case "$1" in
            --confirm) CONFIRM=1; shift ;;
            --chain) custom_chain="${2:-}"; shift 2 ;;
            --evidence-dir) EVIDENCE_DIR="${2:-}"; shift 2 ;;
            -h|--help) usage; exit 0 ;;
            -*)
                if in_allowlist "$1"; then
                    command="$1"; shift
                else
                    die "unknown option: $1. Try -h for help."
                fi
                ;;
            *)
                if [ -z "$command" ] && in_allowlist "$1"; then
                    command="$1"; shift
                else
                    die "unknown argument: $1"
                fi
                ;;
        esac
    done

    [ -z "$command" ] && { usage; exit 2; }

    # Override chain if provided
    [ -n "$custom_chain" ] && CHAIN_ID="$custom_chain"

    # Set up evidence directory
    if [ -z "$EVIDENCE_DIR" ]; then
        EVIDENCE_DIR="$AGENT_DIR/product-flows/gov-toggles/$TIMESTAMP"
    fi
    mkdir -p "$EVIDENCE_DIR"

    echo "╔══════════════════════════════════════════════════════╗"
    echo "║  NexaRail Product-Flow Governance Toggle            ║"
    echo "║  TESTNET/DEVNET ONLY                                ║"
    echo "╚══════════════════════════════════════════════════════╝"
    echo "command:   $command"
    echo "chain:     $CHAIN_ID"
    echo "evidence:  $EVIDENCE_DIR"
    echo "confirm:   $([[ $CONFIRM -eq 1 ]] && echo yes || echo no dry-run)"
    echo ""

    # Validate binary
    if [ ! -x "$BINARY" ]; then
        die "binary not found at $BINARY. Run 'make build' first."
    fi

    # -----------------------------------------------------------------------
    # show-live-flags — just query and exit
    # -----------------------------------------------------------------------
    if [ "$command" = "show-live-flags" ]; then
        show_live_flags "$EVIDENCE_DIR"
        write_evidence "$command" "show-live-flags"
        exit 0
    fi

    # -----------------------------------------------------------------------
    # Safety checks before any enable that has dependency constraints
    # -----------------------------------------------------------------------
    check_safety "$command" "$EVIDENCE_DIR"

    # -----------------------------------------------------------------------
    # Determine target flag changes and build the proposal message
    # -----------------------------------------------------------------------
    local msg_json=""
    local label=""
    local target_value

    case "$command" in
        enable-escrow-live)
            target_value="true"; label="gov-enable-escrow-live"
            msg_json="$(build_escrow_proposal_msg "$EVIDENCE_DIR" true)"
            ;;
        disable-escrow-live)
            target_value="false"; label="gov-disable-escrow-live"
            msg_json="$(build_escrow_proposal_msg "$EVIDENCE_DIR" false)"
            ;;
        enable-settlement-live)
            target_value="true"; label="gov-enable-settlement-live"
            # preserve current treasury/burn routing from on-chain params
            load_settlement_params "$EVIDENCE_DIR"
            local cur_treasury cur_burn
            cur_treasury="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.treasury_routing_enabled // "false"')"
            cur_burn="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.burn_routing_enabled // "false"')"
            msg_json="$(build_settlement_proposal_msg "$EVIDENCE_DIR" true "$cur_treasury" "$cur_burn")"
            ;;
        disable-settlement-live)
            target_value="false"; label="gov-disable-settlement-live"
            load_settlement_params "$EVIDENCE_DIR"
            local cur_treasury cur_burn
            cur_treasury="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.treasury_routing_enabled // "false"')"
            cur_burn="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.burn_routing_enabled // "false"')"
            msg_json="$(build_settlement_proposal_msg "$EVIDENCE_DIR" false "$cur_treasury" "$cur_burn")"
            ;;
        enable-settlement-treasury-routing)
            target_value="true"; label="gov-enable-settlement-treasury-routing"
            load_settlement_params "$EVIDENCE_DIR"
            local cur_live cur_burn
            cur_live="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.live_enabled // "false"')"
            cur_burn="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.burn_routing_enabled // "false"')"
            msg_json="$(build_settlement_proposal_msg "$EVIDENCE_DIR" "$cur_live" true "$cur_burn")"
            ;;
        disable-settlement-treasury-routing)
            target_value="false"; label="gov-disable-settlement-treasury-routing"
            load_settlement_params "$EVIDENCE_DIR"
            local cur_live cur_burn
            cur_live="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.live_enabled // "false"')"
            cur_burn="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.burn_routing_enabled // "false"')"
            msg_json="$(build_settlement_proposal_msg "$EVIDENCE_DIR" "$cur_live" false "$cur_burn")"
            ;;
        enable-settlement-burn-routing)
            target_value="true"; label="gov-enable-settlement-burn-routing"
            load_settlement_params "$EVIDENCE_DIR"
            local cur_live cur_treasury
            cur_live="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.live_enabled // "false"')"
            cur_treasury="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.treasury_routing_enabled // "false"')"
            msg_json="$(build_settlement_proposal_msg "$EVIDENCE_DIR" "$cur_live" "$cur_treasury" true)"
            ;;
        disable-settlement-burn-routing)
            target_value="false"; label="gov-disable-settlement-burn-routing"
            load_settlement_params "$EVIDENCE_DIR"
            local cur_live cur_treasury
            cur_live="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.live_enabled // "false"')"
            cur_treasury="$(json_get "$EVIDENCE_DIR/current-settlement-params.json" '.treasury_routing_enabled // "false"')"
            msg_json="$(build_settlement_proposal_msg "$EVIDENCE_DIR" "$cur_live" "$cur_treasury" false)"
            ;;
        enable-treasury-live)
            target_value="true"; label="gov-enable-treasury-live"
            msg_json="$(build_treasury_proposal_msg "$EVIDENCE_DIR" true)"
            ;;
        disable-treasury-live)
            target_value="false"; label="gov-disable-treasury-live"
            msg_json="$(build_treasury_proposal_msg "$EVIDENCE_DIR" false)"
            ;;
        enable-payout-live)
            target_value="true"; label="gov-enable-payout-live"
            msg_json="$(build_payout_proposal_msg "$EVIDENCE_DIR" true)"
            ;;
        disable-payout-live)
            target_value="false"; label="gov-disable-payout-live"
            msg_json="$(build_payout_proposal_msg "$EVIDENCE_DIR" false)"
            ;;
    esac

    if [ -z "$label" ] || [ -z "$msg_json" ]; then
        die "internal error: could not build proposal for command=$command"
    fi

    # Print the proposal JSON
    local title
    title="TESTNET: ${command} -- ${label}"
    cat > "$EVIDENCE_DIR/proposal-print.json" <<EOF
{
  "title": "$title",
  "summary": "Governance toggle on agent-testnet. TESTNET ONLY — tokens have zero value. No mainnet implications.",
  "messages": [
$msg_json
  ],
  "metadata": "",
  "deposit": "1000000$DENOM",
  "expedited": false
}
EOF

    echo ""
    echo "--- Proposal summary ---"
    echo "Module: $(echo "$label" | sed 's/^gov-//; s/-enable-.*//; s/-disable-.*//')"
    echo "Target: $command"
    echo "Proposal JSON written: $EVIDENCE_DIR/proposal-print.json"
    echo ""
    echo "Proposal messages:"
    echo "$msg_json" | jq '.' 2>/dev/null || echo "$msg_json"

    # -----------------------------------------------------------------------
    # Dry-run mode — just print and exit
    # -----------------------------------------------------------------------
    if [ "$CONFIRM" -eq 0 ]; then
        echo ""
        warn "DRY-RUN — no action taken. Re-run with --confirm to submit."
        echo ""
        info "To submit this proposal:"
        echo "  $0 $command --confirm"
        info "To re-run in dry-run mode:"
        echo "  $0 $command"
        info "Evidence from this dry run: $EVIDENCE_DIR"
        echo ""

        write_evidence "$command" "$label"
        exit 0
    fi

    # -----------------------------------------------------------------------
    # Confirm mode — submit governance proposal
    # -----------------------------------------------------------------------
    echo ""
    info "CONFIRMED — submitting governance proposal: $command"
    info "Evidence: $EVIDENCE_DIR"
    echo ""

    submit_gov_proposal "$label" "$EVIDENCE_DIR/gov" "$title" "$msg_json"

    local proposal_id tx_hash
    proposal_id="$(cat "$EVIDENCE_DIR/gov/proposal-id.txt" 2>/dev/null || echo "")"
    tx_hash="$(cat "$EVIDENCE_DIR/gov/txhash.txt" 2>/dev/null || echo "")"

    echo ""
    echo "╔══════════════════════════════════════════════════════╗"
    echo "║  Governance toggle complete                          ║"
    echo "╚══════════════════════════════════════════════════════╝"
    echo "Command:      $command"
    echo "Proposal ID:  $proposal_id"
    echo "Tx hash:      $tx_hash"
    echo "Evidence:     $EVIDENCE_DIR"
    echo "Rerun:        $0 $command --confirm --chain $CHAIN_ID"
    echo ""
    info "If the flag is still not correct, check evidence and re-run the toggle."

    write_evidence "$command" "$label" "$proposal_id" "$tx_hash"

    # Verify the flag changed
    echo ""
    echo "--- Post-toggle flag verification ---"
    show_live_flags "$EVIDENCE_DIR/post-verify"
}

main "$@"
