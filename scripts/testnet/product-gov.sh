# ---------------------------------------------------------------------------
# Proposal submission & governance lifecycle
# ---------------------------------------------------------------------------
submit_gov_proposal() {
    local label="$1"
    local dir="$2"
    local title="$3"
    local msg_json="$4"

    local bravo_addr account_number sequence proposal_id tx_hash
    mkdir -p "$dir"

    bravo_addr="$(agent_addr "$SUBMIT_AGENT")"
    read -r account_number sequence < <(account_numbers "$bravo_addr" "$dir/bravo-account.json") \
        || die "cannot read account numbers for $bravo_addr. Check that the agent key exists at $SUBMIT_HOME and the node is reachable at $BRAVO_RPC."
    echo "account_number=$account_number sequence=$sequence" > "$dir/signing-account.txt"

    # Write proposal JSON
    cat > "$dir/proposal.json" <<EOF
{
  "title": "$title",
  "summary": "Governance toggle on agent-testnet. TESTNET ONLY — tokens have zero value. No mainnet implications.",
  "messages": [
$msg_json
  ],
  "metadata": "",
  "deposit": "1000000$DENOM",
  "expedited": false
}
EOF

    info "generating unsigned proposal..."
    if ! "$BINARY" tx gov submit-proposal "$dir/proposal.json" \
        --from "$SUBMIT_KEY" --keyring-backend test --home "$SUBMIT_HOME" \
        --chain-id "$CHAIN_ID" --node "$BRAVO_RPC" \
        --generate-only --fees "$TX_FEE" \
        > "$dir/unsigned.json" 2> "$dir/unsigned.err"; then
        die "proposal generate-only failed. See $dir/unsigned.err for details."
    fi

    info "signing proposal..."
    if ! "$BINARY" tx sign "$dir/unsigned.json" \
        --offline --account-number "$account_number" --sequence "$sequence" \
        --from "$SUBMIT_KEY" --keyring-backend test --home "$SUBMIT_HOME" \
        --chain-id "$CHAIN_ID" \
        > "$dir/signed.json" 2> "$dir/signed.err"; then
        die "proposal signing failed. Common cause: account sequence mismatch if a prior tx was broadcast but not confirmed. See $dir/signed.err and $dir/signing-account.txt."
    fi

    # Broadcast via raw CometBFT RPC (same pattern as rehearsal)
    info "broadcasting signed proposal..."
    local encoded_b64
    encoded_b64="$("$BINARY" tx encode "$dir/signed.json" | tr -d '\n')"
    echo -n "$encoded_b64" > "$dir/signed.b64"

    curl -s --max-time 15 "$BRAVO_RPC_HTTP/" \
        -H "Content-Type: application/json" \
        -d "{\"jsonrpc\":\"2.0\",\"method\":\"broadcast_tx_sync\",\"params\":{\"tx\":\"${encoded_b64}\"},\"id\":1}" \
        > "$dir/broadcast-cometbft.json"

    tx_hash="$(json_get "$dir/broadcast-cometbft.json" '.result.hash // ""')"
    local code check_tx_code
    code="$(json_get "$dir/broadcast-cometbft.json" '.result.code // 1')"
    check_tx_code="$(json_get "$dir/broadcast-cometbft.json" '.result.code // 1')"
    echo "$tx_hash" > "$dir/txhash.txt"
    echo "$code" > "$dir/checktx-code.txt"

    if [ -z "$tx_hash" ] || [ "$code" != "0" ]; then
        die "broadcast failed tx_hash=${tx_hash:-missing} code=${code:-missing}. Check $dir/broadcast-cometbft.json for full response."
    fi
    ok "proposal broadcast tx_hash=$tx_hash"

    # Wait for inclusion
    if ! wait_for_tx "$tx_hash" "$dir/submit-tx.json" "$label"; then
        die "proposal tx $tx_hash not included before timeout"
    fi

    proposal_id="$(latest_proposal_id "$dir/proposals-after-submit.json")" \
        || die "cannot read back proposal ID after submission"
    echo "$proposal_id" > "$dir/proposal-id.txt"
    ok "proposal ID=$proposal_id"

    # Vote all agents Yes
    vote_all_yes "$proposal_id" "$dir" "$label"

    # Wait for passage
    wait_for_proposal_passed "$proposal_id" "$dir" "$label" || {
        local final_status
        final_status="$(cat "$dir/proposal-status-latest.txt" 2>/dev/null || echo "unknown")"
        warn "proposal $proposal_id did not pass; final status=$final_status"
        echo "$final_status" > "$dir/final-status.txt"
        return 1
    }
}

# ---------------------------------------------------------------------------
# Governance lifecycle primitives (from rehearsal patterns)
# ---------------------------------------------------------------------------
wait_for_tx() {
    local tx_hash="$1"
    local outfile="$2"
    local label="$3"
    local rpc_hash

    rpc_hash="$tx_hash"
    case "$rpc_hash" in 0x*|0X*) ;; *) rpc_hash="0x$rpc_hash" ;; esac

    for _ in $(seq 1 30); do
        curl -s --max-time 10 "$BRAVO_RPC_HTTP/tx?hash=$rpc_hash" > "$outfile" 2>/dev/null || true
        if jq -e '.result.hash' "$outfile" >/dev/null 2>&1; then
            local code
            code="$(json_get "$outfile" '.result.tx_result.code // 0')"
            if [ "$code" = "0" ]; then
                ok "$label included code=0"
                return 0
            fi
            die "$label included code=$code"
        fi
        sleep 2
    done
    die "$label not found by CometBFT tx query"
}

wait_for_proposal_passed() {
    local proposal_id="$1"
    local dir="$2"
    local label="$3"
    local status
    local waited=0

    while [ "$waited" -lt "$PROPOSAL_TIMEOUT_SEC" ]; do
        status="$(proposal_status "$proposal_id" "$dir/proposal-status-${waited}.json" 2>/dev/null || echo "UNKNOWN")"
        echo "$status" > "$dir/proposal-status-latest.txt"
        if [ "$status" = "PROPOSAL_STATUS_PASSED" ]; then
            cp "$dir/proposal-status-${waited}.json" "$dir/proposal-final-status.json"
            ok "$label proposal $proposal_id passed"
            return 0
        fi
        if [ "$status" = "PROPOSAL_STATUS_FAILED" ] || [ "$status" = "PROPOSAL_STATUS_REJECTED" ]; then
            cp "$dir/proposal-status-${waited}.json" "$dir/proposal-final-status.json"
            warn "$label proposal $proposal_id final status=$status. Check $dir/proposal-status-*.json for the full proposal status record."
            return 1
        fi
        sleep 5
        waited=$((waited + 5))
    done

    warn "$label proposal $proposal_id did not pass before ${PROPOSAL_TIMEOUT_SEC}s timeout; latest=$status. Common causes: insufficient deposit, voting power not reached, or node not progressing."
    echo "  [WARN] Check $dir/proposal-status-*.json for status history." >&2
    return 1
}

vote_all_yes() {
    local proposal_id="$1"
    local dir="$2"
    local label="$3"
    mkdir -p "$dir/votes"
    : > "$dir/vote-tx-hashes.txt"

    for agent in "${AGENTS[@]}"; do
        local outfile="$dir/votes/${agent}-vote.json"
        if ! "$BINARY" tx gov vote "$proposal_id" yes \
            --from "${agent}-key" --keyring-backend test \
            --home "$(agent_home "$agent")" \
            --chain-id "$CHAIN_ID" \
            --node "$BRAVO_RPC" \
            --fees "2000$DENOM" \
            --broadcast-mode sync \
            --output json \
            -y > "$outfile" 2>/dev/null; then
            warn "$label $agent vote command failed. Check that agent '$agent' key exists at $(agent_home "$agent")."
            continue
        fi
        local tx_hash code
        tx_hash="$(json_get "$outfile" '.txhash // ""')"
        code="$(json_get "$outfile" '.code // 0')"
        if [ -n "$tx_hash" ] && [ "$code" = "0" ]; then
            echo "$agent $tx_hash" >> "$dir/vote-tx-hashes.txt"
            ok "$label $agent vote submitted tx_hash=$tx_hash"
        else
            warn "$label $agent vote tx_hash=${tx_hash:-missing} code=${code:-missing}. Check $outfile for broadcast response."
        fi
        sleep 1
    done
}

# ---------------------------------------------------------------------------
