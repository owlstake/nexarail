#!/usr/bin/env bash
# NexaRail — product-flow governance flag helper
#
# TESTNET/DEVNET ONLY. Tokens have zero value. No mainnet exists.
#
# Toggles product-flow live/routing flags through the governance proposal
# path. Uses MsgUpdateParams for the relevant module. Always reads current
# on-chain params and preserves all fields except the target flag(s).
#
# Usage:
#   product-gov.sh enable-escrow-live [--confirm]
#   product-gov.sh disable-escrow-live [--confirm]
#   product-gov.sh enable-settlement-live [--confirm]
#   product-gov.sh disable-settlement-live [--confirm]
#   product-gov.sh enable-settlement-treasury-routing [--confirm]
#   product-gov.sh disable-settlement-treasury-routing [--confirm]
#   product-gov.sh enable-settlement-burn-routing [--confirm]
#   product-gov.sh disable-settlement-burn-routing [--confirm]
#   product-gov.sh enable-treasury-live [--confirm]
#   product-gov.sh disable-treasury-live [--confirm]
#   product-gov.sh enable-payout-live [--confirm]
#   product-gov.sh disable-payout-live [--confirm]
#   product-gov.sh show-live-flags
#
# Options:
#   --confirm         Submit the governance proposal, vote Yes, and wait for
#                     passage. Without this flag, the script runs in dry-run
#                     mode — it prints the proposal JSON and takes no action.
#   --chain           Chain ID (default: nexarail-agent-testnet-1)
#   --evidence-dir    Evidence output directory (auto-generated by default)
#
# Safety rules:
#   - burn routing  requires settlement live + treasury routing
#   - treasury routing  requires settlement live
set -Eeuo pipefail

# ---------------------------------------------------------------------------
# Paths & constants
# ---------------------------------------------------------------------------
PROJECT_DIR="$(cd "$(dirname "$0")" && cd ../.. && pwd)"
SCRIPT_DIR="$PROJECT_DIR/scripts/testnet"
BINARY="$PROJECT_DIR/build/nexaraild"
AGENT_DIR="$PROJECT_DIR/rehearsals/validator-agents"
CHAIN_ID="nexarail-agent-testnet-1"
DENOM="unxrl"
TX_FEE="10000$DENOM"

# bravo is the governance-proposal submitter (matches rehearsal pattern)
SUBMIT_AGENT="bravo"
SUBMIT_HOME="$AGENT_DIR/$SUBMIT_AGENT"
SUBMIT_KEY="${SUBMIT_AGENT}-key"

# bravo RPC & API ports (local 5-agent layout)
BRAVO_RPC="tcp://127.0.0.1:27667"
BRAVO_RPC_HTTP="http://127.0.0.1:27667"
BRAVO_API_PORT="1418"

# Governance authority from genesis — the x/gov module account address
GOV_ADDR="nxr10d07y265gmmuvt4z0w9aw880jnsr700js8jz70"

# Timeout for waiting on the governance proposal to pass
PROPOSAL_TIMEOUT_SEC=120

# Evidence
TIMESTAMP="$(date -u +%Y%m%dT%H%M%SZ)"
EVIDENCE_DIR=""
CONFIRM=0
AGENTS=(alpha bravo charlie delta echo)

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
usage() {
    sed -n '2,/^$/p' "$0" | sed 's/^# //; s/^#$//' | grep -v '^$' | head -5
    grep '^#   product-gov' "$0" | sed 's/^#//'
    sed -n '/^# Options:/,/^# Safety rules:/p' "$0" | sed 's/^# //; s/^#$//'
}

die() {
    echo "[FATAL] $*" >&2
    local rerun_cmd="$0 $command${CONFIRM:+ --confirm}"
    echo "[FATAL] Evidence: ${EVIDENCE_DIR:-not set}" >&2
    echo "[FATAL] Rerun: $rerun_cmd" >&2
    echo "[FATAL] Common causes: node not running, incorrect chain or ports, binary not built," >&2
    echo "[FATAL]   governance authority mismatch, insufficient funds in submitter account." >&2
    exit 1
}

info()  { echo "  [INFO] $*"; }
ok()    { echo "  [OK]   $*"; }
warn()  { echo "  [WARN] $*"; }

# Allowlist of valid subcommands
VALID_COMMANDS=(
    enable-escrow-live disable-escrow-live
    enable-settlement-live disable-settlement-live
    enable-settlement-treasury-routing disable-settlement-treasury-routing
    enable-settlement-burn-routing disable-settlement-burn-routing
    enable-treasury-live disable-treasury-live
    enable-payout-live disable-payout-live
    show-live-flags
)

in_allowlist() {
    local cmd="$1"
    for v in "${VALID_COMMANDS[@]}"; do [ "$v" = "$cmd" ] && return 0; done
    return 1
}

is_true() { [ "${1:-false}" = "true" ] || [ "${1:-false}" = "True" ]; }

# ---------------------------------------------------------------------------
# REST queries
# ---------------------------------------------------------------------------
rest_get() {
    local path="$1"
    local out="$2"
    curl -s --max-time 10 "http://127.0.0.1:$BRAVO_API_PORT$path" > "$out" 2>/dev/null || return 1
    if jq -e 'type == "object" and (has("error") | not) and ((has("code") and ((.code|tostring) != "0")) | not)' "$out" >/dev/null 2>&1; then
        return 0
    fi
    return 1
}

json_get() {
    local file="$1"
    local expr="$2"
    jq -r "$expr" "$file" 2>/dev/null || echo ""
}

agent_home() { printf '%s/%s' "$AGENT_DIR" "$1"; }

agent_addr() {
    local agent="$1"
    "$BINARY" keys show "${agent}-key" -a --keyring-backend test \
        --home "$(agent_home "$agent")" 2>/dev/null || die "cannot resolve $agent address"
}

account_numbers() {
    local addr="$1"
    local outfile="$2"
    "$BINARY" query auth account "$addr" --node "$BRAVO_RPC" --output json > "$outfile" 2>/dev/null || return 1
    python3 - "$outfile" <<'PY'
import json, sys
with open(sys.argv[1]) as f:
    d = json.load(f)
a = d.get("account", d)
if isinstance(a, dict) and "base_account" in a:
    a = a["base_account"]
print(a.get("account_number", "0"), a.get("sequence", "0"))
PY
}

latest_proposal_id() {
    local outfile="$1"
    "$BINARY" query gov proposals --node "$BRAVO_RPC" --output json > "$outfile" 2>/dev/null || return 1
    python3 - "$outfile" <<'PY'
import json, sys
with open(sys.argv[1]) as f:
    d = json.load(f)
props = d.get("proposals", [])
if not props:
    print("")
    raise SystemExit(1)
p = props[-1]
print(p.get("id") or p.get("proposal_id") or "")
PY
}

proposal_status() {
    local proposal_id="$1"
    local outfile="$2"
    "$BINARY" query gov proposal "$proposal_id" --node "$BRAVO_RPC" --output json > "$outfile" 2>/dev/null || return 1
    python3 - "$outfile" <<'PY'
import json, sys
with open(sys.argv[1]) as f:
    d = json.load(f)
p = d.get("proposal", d)
print(p.get("status", "UNKNOWN"))
PY
}

